def Aufgabe1():
    products = ["Bread", "Butter", "Cheese", "Milk", "Eggs"]
    for index, elem in enumerate(products, 1):
        if elem == "Milk":
            print("Товар 'Milk' в списке покупок: ", index)
            break
    else:
        print("Товара нет")

