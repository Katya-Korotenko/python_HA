# Длина слова: 11
#

def Aufgabe3():
    sentence = "Programming in Python is both fun and educational"
    split_sens = sentence.split()
    max_text = max(split_sens, key=len)
    print("Самое длинное слово: ", max_text)
    print("Длина слова: ", len(max_text))



